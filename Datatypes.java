
class Datatypes{
    public static void main(String args[])
    {
        int num = 34567; //assigning an integer
        int num1 = 0b101; //assigning binary values
        int num2 = 0x7E;  //assigning hexadecimal values
        float ff = 234;  //integer converts to float implicity
        double dd = 12e20; //12 raised to 10
        long a = 100000009534l; //it should end with l
        short b = 34;
        byte c = 34;
        float f = 0.3f; //double to float is not possible f must be kept
        double d = 0.000089;
        char ch = 'p';
        char ch1 = ch++;
        String S = "prerana";
        boolean bo = true;
        System.out.println(num);
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(ff);
        System.out.println(dd);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);
        System.out.println(ch1);
        System.out.println(S);
        System.out.println(bo);
    }
}